function add(number1 , number2){
    return console.log(`${number1} + ${number2} = ${number1 + number2}`) ; 
}
function multiply(number1 , number2){
    return console.log(`${number1} * ${number2} = ${number1 * number2}`) ; 
}
function substract(number1 , number2){
    return console.log(`${number1} - ${number2} = ${number1 - number2}`) ; 
}
function divide(number1 , number2){
    return console.log(`${number1} / ${number2} = ${number1 / number2}`) ; 
}
add(12,34);
multiply(23,4);
divide(50,10);
substract(45,23); 